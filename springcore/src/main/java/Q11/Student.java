package Q11;

public class Student {
	

	String studentName;
	int rollNo;
	String dob;
	String className;
	String section;
	Result result;
	
	public Student(String studentName, int rollNo, String dob, String className, String section, Result result) {
		super();
		this.studentName = studentName;
		this.rollNo = rollNo;
		this.dob = dob;
		this.className = className;
		this.section = section;
		this.result = result;
}
}
